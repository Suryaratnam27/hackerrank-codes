def time_conversion(s):
    d={'12':'00',
       '01':'13',
       '02':'14',
       '03':'15',
       '04':'16',
       '05':'17',
       '06':'18',
       '07':'19',
       '08':'20',
       '09':'21',
       '10':'22',
       '11':'23'
       }
    b=''
    if s[-2:]=='PM':
        a=d[s[0:2]]
        b=b+a
        b=b+s[2:-2]
        return b
    else:
        return s[0:-2]
q=time_conversion('04:00:00AM')
print(q)
